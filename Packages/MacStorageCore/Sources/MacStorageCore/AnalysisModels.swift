import Foundation
import CoreGraphics

// MARK: - Dependency Graph

public enum GraphNodeKind: String, Codable, Sendable, CaseIterable {
    case volume
    case application
    case directory
    case package
    case cache
    case file
    case orphan

    public var displayName: String {
        switch self {
        case .volume: return "Volume"
        case .application: return "Application"
        case .directory: return "Directory"
        case .package: return "Package"
        case .cache: return "Cache"
        case .file: return "File"
        case .orphan: return "Orphan"
        }
    }

    public var systemImage: String {
        switch self {
        case .volume: return "externaldrive.fill"
        case .application: return "app.fill"
        case .directory: return "folder.fill"
        case .package: return "shippingbox.fill"
        case .cache: return "bolt.horizontal.circle.fill"
        case .file: return "doc.fill"
        case .orphan: return "exclamationmark.triangle.fill"
        }
    }
}

public enum GraphEdgeKind: String, Codable, Sendable, CaseIterable {
    case owns
    case dependsOn
    case generatedBy
    case sharedWith
    case installedBy

    public var displayName: String {
        switch self {
        case .owns: return "owns"
        case .dependsOn: return "depends on"
        case .generatedBy: return "generated by"
        case .sharedWith: return "shared with"
        case .installedBy: return "installed by"
        }
    }
}

public struct GraphNode: Identifiable, Codable, Sendable, Equatable {
    public var id: String
    public var label: String
    public var kind: GraphNodeKind
    public var path: String?
    public var bytes: Int64
    public var category: StorageCategory?

    public init(
        id: String,
        label: String,
        kind: GraphNodeKind,
        path: String? = nil,
        bytes: Int64 = 0,
        category: StorageCategory? = nil
    ) {
        self.id = id
        self.label = label
        self.kind = kind
        self.path = path
        self.bytes = bytes
        self.category = category
    }
}

public struct GraphEdge: Identifiable, Codable, Sendable, Equatable {
    public var id: String { "\(sourceID)->\(targetID):\(kind.rawValue)" }
    public var sourceID: String
    public var targetID: String
    public var kind: GraphEdgeKind

    public init(sourceID: String, targetID: String, kind: GraphEdgeKind) {
        self.sourceID = sourceID
        self.targetID = targetID
        self.kind = kind
    }
}

public struct DependencyGraph: Codable, Sendable, Equatable {
    public var nodes: [GraphNode]
    public var edges: [GraphEdge]

    public init(nodes: [GraphNode] = [], edges: [GraphEdge] = []) {
        self.nodes = nodes
        self.edges = edges
    }
}

// MARK: - Duplicates

public struct DuplicateGroup: Identifiable, Codable, Sendable, Equatable, Hashable {
    public var id: String { contentHash }
    public var contentHash: String
    public var totalBytes: Int64
    public var wastedBytes: Int64
    public var paths: [String]
    public var fileName: String
    public var matchKind: DuplicateMatchKind

    public init(
        contentHash: String,
        totalBytes: Int64,
        wastedBytes: Int64,
        paths: [String],
        fileName: String,
        matchKind: DuplicateMatchKind = .contentHash
    ) {
        self.contentHash = contentHash
        self.totalBytes = totalBytes
        self.wastedBytes = wastedBytes
        self.paths = paths
        self.fileName = fileName
        self.matchKind = matchKind
    }
}

public enum DuplicateMatchKind: String, Codable, Sendable {
    case contentHash
    case fileName
    case sizeAndName
}

// MARK: - Application orphans

public struct InstalledApplication: Identifiable, Codable, Sendable, Equatable {
    public var id: String { bundleID ?? path }
    public var name: String
    public var path: String
    public var bundleID: String?
    public var supportPaths: [String]
    public var totalSupportBytes: Int64

    public init(
        name: String,
        path: String,
        bundleID: String? = nil,
        supportPaths: [String] = [],
        totalSupportBytes: Int64 = 0
    ) {
        self.name = name
        self.path = path
        self.bundleID = bundleID
        self.supportPaths = supportPaths
        self.totalSupportBytes = totalSupportBytes
    }
}

public struct OrphanArtifact: Identifiable, Codable, Sendable, Equatable {
    public var id: String { path }
    public var path: String
    public var name: String
    public var bytes: Int64
    public var kind: OrphanKind
    public var suspectedOwner: String?
    public var confidence: Double
    public var reason: String

    public init(
        path: String,
        name: String,
        bytes: Int64,
        kind: OrphanKind,
        suspectedOwner: String? = nil,
