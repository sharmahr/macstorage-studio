// MARK: - History / trends

public struct ScanSnapshot: Identifiable, Codable, Sendable, Equatable {
    public var id: UUID
    public var startedAt: Date
    public var finishedAt: Date?
    public var status: ScanStatus
    public var filesScanned: Int
    public var bytesScanned: Int64
    public var categoryTotals: [String: Int64]

    public init(
        id: UUID,
        startedAt: Date,
        finishedAt: Date?,
        status: ScanStatus,
        filesScanned: Int,
        bytesScanned: Int64,
        categoryTotals: [String: Int64] = [:]
    ) {
        self.id = id
        self.startedAt = startedAt
        self.finishedAt = finishedAt
        self.status = status
        self.filesScanned = filesScanned
        self.bytesScanned = bytesScanned
        self.categoryTotals = categoryTotals
    }
}

public struct TrendPoint: Identifiable, Codable, Sendable, Equatable {
    public var id: UUID { sessionID }
    public var sessionID: UUID
    public var date: Date
    public var totalBytes: Int64
    public var fileCount: Int

    public init(sessionID: UUID, date: Date, totalBytes: Int64, fileCount: Int) {
        self.sessionID = sessionID
        self.date = date
        self.totalBytes = totalBytes
        self.fileCount = fileCount
    }
}

public struct CategoryDelta: Identifiable, Codable, Sendable, Equatable {
    public var id: String { category.rawValue }
    public var category: StorageCategory
    public var previousBytes: Int64
    public var currentBytes: Int64

    public var deltaBytes: Int64 { currentBytes - previousBytes }

    public init(category: StorageCategory, previousBytes: Int64, currentBytes: Int64) {
        self.category = category
        self.previousBytes = previousBytes
        self.currentBytes = currentBytes
    }
}

public struct HistoryReport: Codable, Sendable, Equatable {
    public var snapshots: [ScanSnapshot]
    public var trend: [TrendPoint]
    public var categoryDeltas: [CategoryDelta]
    public var largestGrowth: [CategoryDelta]
    public var largestShrink: [CategoryDelta]

    public init(
        snapshots: [ScanSnapshot] = [],
        trend: [TrendPoint] = [],
        categoryDeltas: [CategoryDelta] = [],
        largestGrowth: [CategoryDelta] = [],
        largestShrink: [CategoryDelta] = []
    ) {
        self.snapshots = snapshots
        self.trend = trend
        self.categoryDeltas = categoryDeltas
        self.largestGrowth = largestGrowth
        self.largestShrink = largestShrink
    }
}
