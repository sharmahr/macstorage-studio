        self.path = path
        self.name = name
        self.bytes = bytes
        self.kind = kind
        self.suspectedOwner = suspectedOwner
        self.confidence = confidence
        self.reason = reason
    }
}

public enum OrphanKind: String, Codable, Sendable, CaseIterable {
    case supportData
    case preferences
    case caches
    case containers
    case plugins
    case logs
    case leftover

    public var displayName: String {
        switch self {
        case .supportData: return "Application Support"
